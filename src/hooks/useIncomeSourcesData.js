import { useEffect, useState } from 'react';
import { getCachedIncomeSources, getIncomeSources } from '../services/incomeBuilderService';

const defaultState = {
  currency: 'VND',
  incomeSources: [],
  summary: {
    currentMonthlyIncome: 0,
    targetMonthlyIncome: 0,
    gap: 0,
    activeSources: 0,
  },
};

export function useIncomeSourcesData(userId) {
  const [data, setData] = useState(defaultState);
  const [loading, setLoading] = useState(true);
  const [refreshing, setRefreshing] = useState(false);
  const [error, setError] = useState('');

  useEffect(() => {
    if (!userId) return;

    const cached = getCachedIncomeSources(userId);
    if (cached) {
      setData(cached);
      setLoading(false);
      setRefreshing(true);
    } else {
      setLoading(true);
      setRefreshing(false);
    }

    let active = true;
    getIncomeSources(userId, { forceFresh: true })
      .then((value) => {
        if (!active) return;
        setData(value);
        setError('');
      })
      .catch((err) => {
        if (!active) return;
        setError(err.message || 'Failed to load income sources.');
      })
      .finally(() => {
        if (!active) return;
        setLoading(false);
        setRefreshing(false);
      });

    return () => {
      active = false;
    };
  }, [userId]);

  return { data, setData, loading, refreshing, error, setError };
}
